export function Patient(name, DOB, registrationNumber, sex, city, region, ethnicity, language) {
  this.name = name;
  this.DOB = DOB;
  this.registrationNumber = registrationNumber;
  this.sex = sex;
  this.city = city;
  this.region = region;
  this.ethnicity = ethnicity;
  this.language = language;

}
